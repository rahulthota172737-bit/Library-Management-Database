use librarymanagement;

-- Total books.
DELIMITER //

CREATE FUNCTION TotalBooks()
RETURNS INT
DETERMINISTIC
BEGIN
    DECLARE TOTAL INT;

    SELECT COUNT(*)
    INTO TOTAL
    FROM BOOKS;

    RETURN TOTAL;
END //

DELIMITER ;


-- Total members.
DELIMITER //

CREATE FUNCTION TotalMembers()
RETURNS INT
DETERMINISTIC
BEGIN
    DECLARE TOTAL INT;

    SELECT COUNT(*)
    INTO TOTAL
    FROM MEMBERS;

    RETURN TOTAL;
END //

DELIMITER ;


-- Books by author.
DELIMITER //

CREATE FUNCTION BooksByAuthor(AUTH VARCHAR(50))
RETURNS INT
DETERMINISTIC
BEGIN
    DECLARE TOTAL INT;

    SELECT COUNT(*)
    INTO TOTAL
    FROM BOOKS B
    JOIN AUTHORS A
    ON B.AUTHOR_ID = A.AUTHOR_ID
    WHERE A.AUTHOR_NAME = AUTH;

    RETURN TOTAL;
END //

DELIMITER ;

-- Books in a category.
DELIMITER //

CREATE FUNCTION BooksInCategory(CAT VARCHAR(15))
RETURNS INT
DETERMINISTIC
BEGIN
    DECLARE TOTAL INT;

    SELECT COUNT(*)
    INTO TOTAL
    FROM BOOKS B
    JOIN CATEGORIES C
    ON B.CATEGORY_ID = C.CATEGORY_ID
    WHERE C.CATEGORY_NAME = CAT;

    RETURN TOTAL;
END //

DELIMITER ;


-- Total borrow records of a member.
DELIMITER //

CREATE FUNCTION TotalBorrowRecords(MID INT)
RETURNS INT
DETERMINISTIC
BEGIN
    DECLARE TOTAL INT;

    SELECT COUNT(*)
    INTO TOTAL
    FROM BORROW_RECORDS
    WHERE MEMBER_ID = MID;

    RETURN TOTAL;
END //

DELIMITER ;